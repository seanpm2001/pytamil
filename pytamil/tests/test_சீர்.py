import pytest
from pytamil.தமிழ் import எழுத்து
from pytamil.தமிழ் import சீர்



@pytest.mark.parametrize("சீர்_வாய்பாடு, பதம்", \
    [ 
        ("கூவிளம்", "வானினும்"),
        ("கூவிளம்", "நீரினும்"),
        ("புளிமாங்காய்", "உயர்ந்தன்று"),
    ])
def test_கூவிளம்(சீர்_வாய்பாடு, பதம்):   
    புது_சீர்_வாய்பாடு = சீர்.சீர்_வாய்பாடு_கொடு(பதம்)

    assert சீர்_வாய்பாடு == புது_சீர்_வாய்பாடு
    
    
	

